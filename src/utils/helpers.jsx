// Loại bỏ emoji ở đầu chuỗi
export const removeLeadingEmoji = (str) => {
  if (!str) return str;
  return str
    .replace(/^(\p{Emoji_Presentation}|\p{Emoji}\uFE0F|\p{Extended_Pictographic})+/gu, '')
    .replace(/^\s+/, '')
    .trim();
};

